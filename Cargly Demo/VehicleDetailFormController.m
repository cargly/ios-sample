- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    //static NSString *CellIdentifier = @"Cell";
    //UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier forIndexPath:indexPath];
    
    // Configure the cell...
    
    switch (indexPath.row) {
        case 0:
            self.carNameCell = [tableView dequeueReusableCellWithIdentifier:@"carName"];
            [self.carNameCell bindAttribute:self.detailItem attributeName:@"name"];
            return self.carNameCell;
        case 1:
            self.postalCodeCell = [tableView dequeueReusableCellWithIdentifier:@"postalCode"];
            [self.postalCodeCell bindAttribute:self.detailItem attributeName:@"postalCode"];
            return self.postalCodeCell;
        default:
            break;
    }
    
    return Nil;
}

